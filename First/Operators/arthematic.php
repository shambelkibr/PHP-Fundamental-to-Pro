<?php
$a = 10;
$b = 5;

echo  "$a + $b = " ,($a + $b) . "<br/>"; // 15 (Addition)
echo "$a - $b = " ,($a - $b) . "<br/>"; // 5  (Subtraction)
echo "$a * $b = " , ($a * $b) . "<br/>"; // 50 (Multiplication)
echo "$a / $b = " , ($a / $b) . "<br/>"; // 2  (Division)
echo "$a % $b = " . ($a % $b) . "<br/>"; // 0  (Modulus - remainder)
echo "$a ** $b = " . ($a ** $b) . "<br/>"; // 100000 (Power)
?>